export function chooseSalesMessage(level: RiskLevel, seed: string) {
  let hash = 0;
  for (const character of seed) hash = (hash * 31 + character.charCodeAt(0)) | 0;
  const options = messages[level];
  return options[Math.abs(hash) % options.length];
}
